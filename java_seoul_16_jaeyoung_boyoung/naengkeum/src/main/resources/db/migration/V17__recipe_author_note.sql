-- =====================================================================
--  V17__recipe_author_note.sql
--  레시피 작성자 소감(author_note) 추가
--
--  목적: AI 추천 레시피를 "내 레시피로 담기" 할 때, 작성자 본인의 간단한
--        생각·소감(예: "아이가 잘 먹었어요", "다음엔 매콤하게")을 함께 남길 수 있게 한다.
--        AI가 생성한 summary 와는 별개의 "사람의 한마디"로 상세 화면에 노출한다.
--
--  - 공공(시드) 레시피는 NULL.
--  - 길이는 한두 문장 수준의 짧은 메모를 가정(최대 500자).
-- =====================================================================

ALTER TABLE recipe
    ADD COLUMN author_note VARCHAR(500) DEFAULT NULL COMMENT '작성자 소감(담을 때 본인이 남긴 한마디)' AFTER summary;
