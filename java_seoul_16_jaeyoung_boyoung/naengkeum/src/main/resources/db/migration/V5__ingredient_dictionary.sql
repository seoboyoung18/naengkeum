-- =====================================================================
--  V5__ingredient_dictionary.sql
--  식재료 사전 (보관기한 마스터)
--
--  목적: 사용자가 냉장고에 식재료 입력 시 보관 위치/유통기한/팁 자동 제안
--  출처: 식약처 소비기한 참고값 + 일반 식품 보관 가이드 기반 큐레이션
--  규모: 150종 (채소/과일/육류/수산물/유제품/계란두부/곡류면/양념/가공)
--
--  보관 일수는 "개봉/구매 후 적정 보관 시 권장 소비 기한"의 일반적 기준이며,
--  실제 제품의 표시 소비기한과 다를 수 있음 (참고용 제안값).
-- =====================================================================

-- ---------------------------------------------------------------------
--  테이블 생성
-- ---------------------------------------------------------------------
CREATE TABLE ingredient_dictionary (
    ingredient_dict_id   BIGINT       NOT NULL AUTO_INCREMENT,
    name                 VARCHAR(50)  NOT NULL                      COMMENT '식재료명',
    category             VARCHAR(30)  NOT NULL                      COMMENT '분류 (채소/과일/육류/수산물/유제품/계란두부/곡류면/양념/가공)',
    default_storage_type ENUM('FRIDGE','FREEZER','ROOM_TEMP') NOT NULL COMMENT '권장 보관 위치',
    fridge_days          INT          DEFAULT NULL                  COMMENT '냉장 보관 가능 일수',
    freezer_days         INT          DEFAULT NULL                  COMMENT '냉동 보관 가능 일수',
    room_temp_days       INT          DEFAULT NULL                  COMMENT '실온 보관 가능 일수',
    storage_tip          VARCHAR(200) DEFAULT NULL                  COMMENT '보관 팁',
    created_at           DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (ingredient_dict_id),
    -- [방어] 식재료명 중복 방지 (자동완성/매칭 정확도)
    UNIQUE KEY uq_ingredient_dict_name (name),
    -- [방어] 보관 일수는 양수 또는 NULL
    CONSTRAINT chk_id_fridge_days  CHECK (fridge_days    IS NULL OR fridge_days    > 0),
    CONSTRAINT chk_id_freezer_days CHECK (freezer_days   IS NULL OR freezer_days   > 0),
    CONSTRAINT chk_id_room_days    CHECK (room_temp_days IS NULL OR room_temp_days > 0)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='식재료 사전 (보관기한 마스터)';

-- ---------------------------------------------------------------------
--  분류별 조회용 인덱스
-- ---------------------------------------------------------------------
CREATE INDEX idx_ingredient_dict_category ON ingredient_dictionary (category);

