-- =====================================================================
--  V19__recipe_ingredients_consumed.sql
--  "공개하기" 시 1회만 냉장고 재고를 차감하기 위한 소비 완료 플래그.
--
--  목적: 레시피를 공개(is_public=TRUE)하는 순간 사용 재료를 냉장고에서 차감한다.
--        단, 공개→비공개→재공개를 반복해도 차감은 "최초 1회"로 고정해야 한다
--        (비공개 전환 시 재고 회복은 하지 않는 정책이므로, 재공개 때 또 빼면
--         한 레시피로 재고가 계속 깎이는 버그가 됨).
--
--  설계: recipe 행에 소비 완료 여부를 둔다. 최초 공개 차감 직후 TRUE로 고정.
--        이후 publish는 이 플래그가 FALSE일 때만 차감을 수행한다.
-- =====================================================================

ALTER TABLE recipe
    ADD COLUMN ingredients_consumed BOOLEAN NOT NULL DEFAULT FALSE
        COMMENT '공개 시 재고 차감을 이미 수행했는지(재공개 중복 차감 방지)';

-- 이 기능 도입 이전에 '이미 공개된' 레시피는 소급 차감 대상이 아니다.
-- (도입 후 다시 publish를 눌렀을 때 과거 재료가 갑자기 빠지는 일을 방지)
-- → 기존 공개 레시피는 '소비 완료'로 표시해 두고, 신규 공개부터만 차감한다.
UPDATE recipe SET ingredients_consumed = TRUE WHERE is_public = TRUE;
