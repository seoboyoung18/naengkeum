-- =====================================================================
--  V8__add_member_role.sql
--  관리자 권한 모델 도입 (회원 역할 USER / ADMIN)
--
--  목적: 배포 운영을 위해 "관리자"를 도입. 관리자는 부적절한 콘텐츠
--        (리뷰/레시피/댓글)를 직접 삭제하고 사용자를 차단할 수 있다.
--        (신고 기능은 만들지 않음 — 관리자가 직접 보고 삭제하는 방식, 팀 합의 2026-06-12)
--
--  새 컬럼 (member):
--    - role : 회원 역할. 'USER'(기본) 또는 'ADMIN'.
--
--  인가 방식:
--    - 기존 "작성자 본인만 수정/삭제"(verifyOwner)에 "또는 관리자" 예외를 추가.
--    - 관리자는 별도 삭제 API 없이 기존 DELETE를 그대로 사용(verifyOwner가 허용).
--    - JWT에 role claim을 실어 필터에서 ROLE_ADMIN/ROLE_USER 권한으로 변환.
-- =====================================================================

-- ---------------------------------------------------------------------
--  1. role 컬럼 추가 (기본 USER)
-- ---------------------------------------------------------------------
ALTER TABLE member
    ADD COLUMN role VARCHAR(10) NOT NULL DEFAULT 'USER'
        COMMENT '회원 역할 (USER / ADMIN)' AFTER is_active;

-- ---------------------------------------------------------------------
--  2. 운영자 계정 지정
--     - 데모/운영용으로 1번(개발자 본인)·2번(페어) 회원을 관리자로 승격.
--     - 실제 가입 계정 ID에 맞게 조정 가능. 존재하지 않는 ID면 0행 갱신(무해).
-- ---------------------------------------------------------------------
UPDATE member SET role = 'ADMIN' WHERE member_id IN (1, 2);

-- ---------------------------------------------------------------------
--  3. 인덱스 (관리자 목록/필터 조회용 — 선택적이지만 가벼움)
-- ---------------------------------------------------------------------
ALTER TABLE member
    ADD KEY idx_member_role (role);