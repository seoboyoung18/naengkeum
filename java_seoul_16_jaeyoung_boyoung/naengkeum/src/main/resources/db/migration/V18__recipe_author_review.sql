-- =====================================================================
--  V18__recipe_author_review.sql
--  레시피 작성자 후기(author_review) 추가
--
--  목적: 작성자가 자신의 레시피 상세 페이지에서 "직접 만들어 본 후기"를
--        남길 수 있게 한다. 등록 시 한 줄 소감(author_note, "작성자 한마디")과는
--        별개로, 조리 후 자유롭게 적고 수정하는 후기 칸이다.
--
--  - 공공(시드) 레시피는 NULL.
--  - 한 문단 수준을 가정(최대 1000자).
-- =====================================================================

ALTER TABLE recipe
    ADD COLUMN author_review VARCHAR(1000) DEFAULT NULL
        COMMENT '작성자 후기(상세 페이지에서 본인이 작성/수정)' AFTER author_note;
